package com.alibaba.citrus.service.resource;

/**
 * 代表一个<code>&lt;resource&gt;</code>、<code>&lt;resource-alias&gt;</code>或
 * <code>&lt;resource-filters&gt;</code>的匹配结果。
 * 
 * @author Michael Zhou
 */
public interface ResourceMatchResult {
    /**
     * 取得已经匹配的名称。
     */
    String getResourceName();

    /**
     * 将匹配部分替换成指定字符串。
     */
    String substitute(String substitution);
}
